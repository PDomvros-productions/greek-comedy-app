let currentQuestionIndex = 0;
const characterScores = {}; // Object to store votes for each character

const quizContentDiv = document.getElementById('quiz-content');
const nextButton = document.getElementById('next-button');
const resultSection = document.getElementById('result-section');
const characterResultDiv = document.getElementById('character-result');
const restartButton = document.getElementById('restart-button');

function loadQuestion() {
    if (currentQuestionIndex < questions.length) {
        const q = questions[currentQuestionIndex];
        let html = `<h3>${q.question}</h3><div class="options">`;
        q.options.forEach((option, index) => {
            html += `
                <label>
                    <input type="radio" name="current_question" value="${option.character}" id="q${currentQuestionIndex}o${index}"> ${option.text}
                </label>
            `;
        });
        html += `</div>`;
        quizContentDiv.innerHTML = html;
        nextButton.textContent = currentQuestionIndex === questions.length - 1 ? "Δες το αποτέλεσμα" : "Επόμενη Ερώτηση";
    } else {
        showResult();
    }
}

function recordAnswer() {
    const selectedOption = document.querySelector('input[name="current_question"]:checked');
    if (selectedOption) {
        const characterName = selectedOption.value;
        characterScores[characterName] = (characterScores[characterName] || 0) + 1;
        currentQuestionIndex++;
        loadQuestion();
    } else {
        alert("Παρακαλώ επιλέξτε μια απάντηση πριν προχωρήσετε.");
    }
}

function showResult() {
    quizContentDiv.style.display = 'none';
    nextButton.style.display = 'none';
    resultSection.style.display = 'block';

    let maxScore = 0;
    let matchingCharacter = null;
    let tiedCharacters = [];

    // Find the max score
    for (const char in characterScores) {
        if (characterScores[char] > maxScore) {
            maxScore = characterScores[char];
        }
    }

    // Find all characters with the max score
    for (const char in characterScores) {
        if (characterScores[char] === maxScore) {
            tiedCharacters.push(char);
        }
    }

    // If there are ties, pick one randomly from the tied characters
    if (tiedCharacters.length > 0) {
        matchingCharacter = tiedCharacters[Math.floor(Math.random() * tiedCharacters.length)];
    } else {
        // Fallback if no character received any votes (shouldn't happen with forced selection)
        matchingCharacter = "Άγνωστος Χαρακτήρας"; // Ή κάποιο άλλο προεπιλεγμένο μήνυμα
    }

    if (characters[matchingCharacter]) {
        const charData = characters[matchingCharacter];
        characterResultDiv.innerHTML = `
            <h3>${matchingCharacter}</h3>
            <p><strong>Σειρά:</strong> ${charData.series}</p>
            <p><strong>Ηθοποιός:</strong> ${charData.actor}</p>
            <p><strong>Περιγραφή:</strong> ${charData.description}</p>
        `;
    } else {
        // Μήνυμα αν δεν βρεθεί χαρακτήρας (π.χ. λόγω καθόλου επιλογών ή σφάλματος)
        characterResultDiv.innerHTML = `<p>Με βάση τις απαντήσεις σας, μοιάζετε με έναν μοναδικό συνδυασμό χαρακτήρων! Είστε μοναδικοί!</p>`;
    }
    restartButton.style.display = 'block';
}

function restartQuiz() {
    currentQuestionIndex = 0;
    for (const char in characterScores) {
        delete characterScores[char]; // Reset scores
    }
    quizContentDiv.style.display = 'block';
    resultSection.style.display = 'none';
    restartButton.style.display = 'none';
    loadQuestion();
}

// Event Listeners
nextButton.addEventListener('click', recordAnswer);
restartButton.addEventListener('click', restartQuiz);

// Initialize the quiz when the page loads
document.addEventListener('DOMContentLoaded', loadQuestion);